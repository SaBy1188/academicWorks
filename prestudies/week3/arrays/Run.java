package de.brightstraining.prestudies.week3.arrays;

public class Run {
        // Zähler läuft von 1 -  10; Schleife läuft bis Zähler endet
    public static void main(String[] args) {
            // Schleife läuft rückwärts von 9 bis 1

        for (int i = 9; i > 0; i--) {
            System.out.println(i);
        }
    }
}